install_cli() {
    echo "${msg_cli_install_start}..."

    ensure_cli_dependencies || return 1

    if ! is_rustminersystem_installed; then
        echo "$msg_cli_main_missing"
        return 1
    fi

    if ! is_cli_installed; then
        check_cli_command_conflict || return 1
    fi

    confirm_cli_install || return 1

    if is_cli_installed; then
        echo "$msg_cli_already_installed"
        create_cli_launcher
        filterResult $? "$msg_cli_install_start"
    else
        mkdir -p "$PATH_RUST"
        chmod 755 "$PATH_RUST"

        echo "${msg_downloading}"
        rm -f -- "$PATH_CLI_TMP"
        download_file "$CLI_DOWNLOAD_URL" "$PATH_CLI_TMP"

