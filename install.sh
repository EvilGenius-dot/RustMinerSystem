SERVICE_FILE="/etc/systemd/system/\${SERVICE_NAME}.service"

show_usage() {
    echo "$text_usage"
}

require_root() {
    if [ "\$(id -u)" != "0" ]; then
        echo "$text_need_root"
        exit 1
    fi
}

is_systemd_available() {
    command -v systemctl >/dev/null 2>&1 && [ -d /run/systemd/system ]
}

is_running() {
    pgrep -x "\$APP_EXEC" >/dev/null 2>&1
}

ensure_app_exists() {
    if [ ! -x "\$APP_PATH" ]; then
        echo "$text_app_missing"
        return 1
    fi
}

open_cli() {
    if [ ! -x "\$CLI_PATH" ]; then
        echo "$text_cli_missing"
        exit 1
    fi

    web_ui=\$(sed -n 's/^[[:space:]]*ENABLE_WEB_UI[[:space:]]*=[[:space:]]*\\([^[:space:]]*\\).*$/\\1/p' "\$CONFIG_FILE" 2>/dev/null | tail -n 1)
    if [ "\$web_ui" != "0" ]; then
        echo "$text_cli_requires_web_disabled"
        exit 1
    fi

    cd "\$APP_DIR" || exit 1
    exec "\$CLI_PATH"
}

start_app() {
    require_root
    ensure_app_exists || return 1

    if is_running; then
        echo "$text_already_running"
        return 0
    fi

    if is_systemd_available && [ -f "\$SERVICE_FILE" ]; then
        systemctl start "\${SERVICE_NAME}.service"
    else
        mkdir -p "\$APP_DIR"
        touch "\$NOHUP_FILE" "\$ERR_FILE"
        chmod 640 "\$NOHUP_FILE" "\$ERR_FILE" 2>/dev/null || true
        nohup "\$APP_PATH" >> "\$NOHUP_FILE" 2>> "\$ERR_FILE" &
    fi

    i=0
    while [ "\$i" -lt 10 ]; do
        if is_running; then
            echo "$text_started"
            return 0
        fi
        sleep 1
        i=\$((i + 1))
    done

    echo "$text_start_failed"
    return 1
}

stop_app() {
    require_root
    was_running=0

    if is_running; then
        was_running=1
    fi

    if is_systemd_available && [ -f "\$SERVICE_FILE" ]; then
        systemctl stop "\${SERVICE_NAME}.service"
    fi

    if is_running; then
        pids=\$(pgrep -x "\$APP_EXEC")
        for pid in \$pids; do
            kill -TERM "\$pid"
        done
    fi

    i=0
    while [ "\$i" -lt 10 ]; do
        if ! is_running; then
            if [ "\$was_running" = "1" ]; then
                echo "$text_stopped"
            else
                echo "$text_not_running"
            fi
            return 0
        fi
        sleep 1
        i=\$((i + 1))
    done

    echo "$text_stop_timeout"
    return 1
}

restart_app() {
    stop_app || return 1
    start_app
}

if [ "\$#" -eq 0 ]; then
    open_cli
fi

case "\$1" in
start)
    start_app
    ;;
stop)
    stop_app
    ;;
restart)
    restart_app
    ;;
help|--help|-h)
    show_usage
    ;;
*)
    echo "$text_unknown: \$1"
    show_usage
    exit 1
    ;;
esac
EOF

    chmod 755 "$CLI_LINK_PATH"
}

